#!/bin/bash

###############################################################################
# Скрипт установки и настройки wg-add-peer.sh
# Устанавливает права на исполнение и проверяет зависимости
###############################################################################

# Цвета для вывода
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

SCRIPT_NAME="wg-add-peer.sh"

echo -e "${YELLOW}Настройка скрипта $SCRIPT_NAME...${NC}"

# Проверяем наличие файла
if [ ! -f "$SCRIPT_NAME" ]; then
    echo -e "${RED}Ошибка: Файл $SCRIPT_NAME не найден!${NC}"
    exit 1
fi

# Устанавливаем права на исполнение
echo -e "${YELLOW}Установка прав на исполнение...${NC}"
chmod +x "$SCRIPT_NAME"

if [ $? -eq 0 ]; then
    echo -e "${GREEN}✓ Права на исполнение установлены${NC}"
else
    echo -e "${RED}Ошибка: Не удалось установить права на исполнение${NC}"
    exit 1
fi

# Проверяем права доступа
if [ -x "$SCRIPT_NAME" ]; then
    echo -e "${GREEN}✓ Скрипт готов к использованию${NC}"
    echo ""
    echo -e "${YELLOW}Для запуска используйте:${NC}"
    echo "  sudo ./$SCRIPT_NAME"
    echo ""
else
    echo -e "${RED}Ошибка: Скрипт не имеет прав на исполнение${NC}"
    exit 1
fi

